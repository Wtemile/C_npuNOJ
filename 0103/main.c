#include<stdio.h>
#include<limits.h>
int main()
{
    int a;
    scanf("%d",&a);
    if (a==1)
    {
        printf("%zu,%d,%d",sizeof(char),CHAR_MIN,CHAR_MAX);
    }
    else if (a==2)
    {
        printf("%zu,%d,%d",sizeof(unsigned char),0,UCHAR_MAX);
    }
    else if (a==3)
    {
        printf("%zu,%d,%d",sizeof(short),SHRT_MIN,SHRT_MAX);
    }
    else if (a==4)
    {
        printf("%zu,%d,%d",sizeof(unsigned short),0,USHRT_MAX);
    }
    else if (a==5)
    {
        printf("%zu,%d,%d",sizeof(int),INT_MIN,INT_MAX);
    }
    else if (a==6)
    {
        printf("%zu,%d,%d",sizeof(unsigned int),0,UINT_MAX);
    }
    else if (a==7)
    {
        printf("%zu,%d,%d",sizeof(long),LONG_MIN,LONG_MAX);
    }
    else if (a==8)
    {
        printf("%zu,%d,%d",sizeof(unsigned long),0,ULONG_MAX);
    }
    else if (a==9)
    {
        printf("%zu,%d,%d",sizeof(long long),LONG_LONG_MIN,LONG_LONG_MAX);
    }
    else if (a==10)
    {
        printf("%zu,%d,%d",sizeof(unsigned long long),0,ULONG_LONG_MAX);
    }

    return 0;
}
